package model;

/**
 * The {@code clockType} enum represents the types of clock modes that can be used in a simulation.
 * It provides two options:
 * <ul>
 *     <li>{@code AUTOMATIC} - The clock advances automatically at a predefined interval.</li>
 *     <li>{@code MANUAL} - The clock advances manually, triggered by user interaction.</li>
 * </ul>
 */
public enum clockType {
    AUTOMATIC,
    MANUAL
}
